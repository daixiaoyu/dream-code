class LRUCache {

    // æ–¹æ¡ˆ1 ï¼š å®ç°ä¸€ä¸ª é˜Ÿåˆ—
    private int capactity;
    private HashMap<Integer, Node> cache;

    private Node head;
    private Node tail;


    public LRUCache(int capacity) {
        this.capactity = capacity;
        this.cache = new HashMap<>();

        head = new Node();
        tail = new Node();
        head.next = tail;
        tail.pre = head;
    }

    public void put(int key, int value) {
        if (cache.containsKey(key)) {
            Node node = cache.get(key);
            node.value = value;
            moveToHead(node);
        } else {
            Node node = new Node();
            node.value = value;
            node.key = key;
            if (cache.size() >= capactity) {
                Node last = tail.pre;
                cache.remove(last.key);
                removeNode(last);
            }
            addToHead(node);
            cache.put(key, node);
        }
    }

    public int get(int key) {
        if (cache.containsKey(key)) {
            Node node = cache.get(key);
            moveToHead(node);
            return node.value;
        }
        return -1;
    }

    public void addToHead(Node node) {

        node.pre = head;
        node.next = head.next;

        head.next.pre = node;
        head.next = node;
    }

    public void moveToHead(Node node) {
        removeNode(node);
        addToHead(node);
    }

    public void removeNode(Node node) {
        node.pre.next = node.next;
        node.next.pre = node.pre;
    }

    class Node {
        Node pre;
        Node next;
        Integer key;
        Integer value;
    }
}

