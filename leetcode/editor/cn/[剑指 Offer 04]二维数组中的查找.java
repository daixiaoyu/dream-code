class Solution {
    public boolean findNumberIn2DArray(int[][] matrix, int target) {
        if (null == matrix || matrix.length == 0 || matrix[0].length == 0) {
            return false;
        }
        int xLengh = matrix.length, yLength = matrix[0].length;
        int x = 0, y = yLength - 1;
        while (x < xLengh && y >= 0) {
            int num = matrix[x][y];
            if (num == target) {
                return true;
            } else if (num > target) {
                y--;
            } else {
                x++;
            }
        }
        return false;

    }
}
